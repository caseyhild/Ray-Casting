    private final int[][] floorMap;
    private final ArrayList<Texture> textures;
    private final Camera camera;
    private final Screen screen;

    public RayCastingFogTest() {
        //set size of screen
        width = 640;
        height = 480;

        //set starting frame

        //initial map and location
        int mapWidth = map.length;
        int mapHeight = map[0].length;
        camera = new Camera(mapWidth/2 + 0.5, mapHeight/2 + 0.5, 1, 0, 0, -0.66);
        floorMap = new int[mapWidth][mapHeight];

        //fog settings
        Vector3D fogColor = new Vector3D(192, 192, 192);
        double visibility = 1.5;

        //what will be displayed to the user and each pixel of that image
        thread = new Thread(this);
        image = new BufferedImage(width, height, BufferedImage.TYPE_INT_RGB);
        pixels = ((DataBufferInt)image.getRaster().getDataBuffer()).getData();

        //list of the available textures to use
        textures = new ArrayList<>();
        textures.add(Texture.bricks);
        textures.add(Texture.grass);
        textures.add(new Texture(64, "solidColor", new Vector3D(fogColor)));

        //starting floor, ceiling, and finish location
        for(int mapX = 0; mapX < mapWidth; mapX++)
        {
            for(int mapY = 0; mapY < mapHeight; mapY++)
            {
                floorMap[mapX][mapY] = 2;
            }
        }

        //keyboard input
        addKeyListener(camera);

        //mouse input
        addMouseListener(camera);
        addMouseMotionListener(camera);

        //send info to screen class to be drawn
        screen = new Screen(map, floorMap, mapWidth, mapHeight, textures, fogColor, visibility, width, height);
        screen.updateGame(camera, pixels, map, floorMap);

        //setting up the window
        setSize(width, height);
        setResizable(false);
        setTitle("Ray Casting Fog Test");
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setBackground(Color.black);
        setLocationRelativeTo(null);
        setVisible(true);
        start();
    }

    private synchronized void start()
    {
        //starts game
        running = true;
        thread.start();
    }

    private void render() throws IOException
    {
        //draws the window
        BufferStrategy bs = getBufferStrategy();
        if(bs == null)
        {
            createBufferStrategy(3);
            return;
        }
        Graphics g = bs.getDrawGraphics();
        g.drawImage(image, 0, 0, image.getWidth(), image.getHeight(), null);
        g.setColor(new Color(0, 0, 0));
        g.fillRect(width/2 - 2, height/2 - 11, 4, 8);
        g.fillRect(width/2 - 2, height/2 + 3, 4, 8);
        g.fillRect(width/2 - 11, height/2 - 2, 8, 4);
        g.fillRect(width/2 + 3, height/2 - 2, 8, 4);
        bs.show();
    }

    public void run()
    {
        //main game loop
        long lastTime = System.nanoTime();
        final double ns = 1000000000.0 / 60.0;//60 times per second
        double delta = 0;
        requestFocus();
        while(running)
        {
            //updates time
            long now = System.nanoTime();
            delta += (now - lastTime) / ns;
            lastTime = now;
            while(delta >= 1)//Make sure update is only happening 60 times a second
            {
                //updates game
                //updating textures
                ArrayList<Texture> texturesCopy = new ArrayList<>(textures);
                textures.clear();
                textures.addAll(texturesCopy);
                //updating camera
                camera.update(map);
                //updating screen
                screen.updateGame(camera, pixels, map, floorMap);
                delta--;
            }
            try
            {
                // Hide the cursor
                BufferedImage cursorImg = new BufferedImage(16, 16, BufferedImage.TYPE_INT_ARGB);
                Cursor blankCursor = Toolkit.getDefaultToolkit().createCustomCursor(
                        cursorImg, new Point(0, 0), "blank cursor");
                this.getContentPane().setCursor(blankCursor);

                render();//displays to the screen unrestricted time
            }
            catch(IOException ignored)
            {

            }
        }
    }

    public static void main(String[] args) {
        new RayCastingFogTest();
    }
